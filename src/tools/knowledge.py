@tool
async def search_knowledge_base(keyword: str) -> str:
    """搜索知识库获取常见问题答案

    Args:
        keyword: 搜索关键词，如"退货"、"发票"、"配送"等
    """
    keyword_lower = keyword.lower()

    # 精确匹配
    if keyword in KNOWLEDGE_BASE:
        info = KNOWLEDGE_BASE[keyword]
        return f"【{info['title']}】\n\n{info['content']}"

    # 模糊匹配
    for key, info in KNOWLEDGE_BASE.items():
        if keyword_lower in key.lower():
            return f"【{info['title']}】\n\n{info['content']}"

    # 关键词联想
    related = []
    for key in KNOWLEDGE_BASE.keys():
        if any(kw in key for kw in [keyword_lower, keyword]):
            related.append(key)

    result = f"未找到关于「{keyword}」的直接答案。\n\n"

    if related:
        result += f"您可能想了解：\n"
        for r in related:
            result += f"  • {r}\n"
        result += f"\n请尝试上述关键词，或联系人工客服。"
    else:
        result += "建议您：\n"
        result += "  • 联系人工客服获取帮助\n"
        result += "  • 拨打客服热线：400-123-4567"

    return result
